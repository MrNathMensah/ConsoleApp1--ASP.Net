namespace CalculatorClassLibrary
{
    public class Calculatorclasslibrary
    {
        
            public double Additions(double num1, double num2)
            {
                return num1 + num2;
            }
            public double Subtractions(double num1, double num2)
            {
                return num1 - num2;
            }
            public double Multiplications(double num1, double num2)
            {
                return num1 * num2;
            }
            public double Division(double num1, double num2)
            {
                return num1 / num2;
            }
        

    }
}